package _4_배열과_리스트;

public class Array3고양이이름_문자열형배열_만들어보기 {

	public static void main(String[] args) {
		
		
		String catName[] = new String[6];
	//  자료형  배열이름[] = new예약어 자료형[배열개수];
		
		catName[0] = "자봉";
		catName[1] = "양이";
		catName[2] = "애기";
		catName[3] = "애동";
		catName[4] = "쿠키";
	//  배열이름[배열순서] = "할당시킬 문자열";
		catName[5] = "";
		catName[6] = "히리";
		
		for (int i = 0; i<5 ; i++) {
			System.out.println(catName[6]);
			
		}
	}

	}

